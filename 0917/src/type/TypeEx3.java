package type;

public class TypeEx3 {
	public static void main(String[] args) {
		/*
		 * 
		 *  자바의 기본자료형 실수형
		 * - float(4바이트) : f를 붙여야한다
		 * 
		 * - double(8바이트) :자바의 기본실수형
		 * 
		 * 
		 */
		
		float f = 0.0f;
		System.out.println(f);
		
		//기본자료형 double 변수 d를 선언하고 3.14로 대입(초기화)
		double d = 3.14;
		
		System.out.println(d);
		
		
	}//main() end
}//TypeEx3 end
